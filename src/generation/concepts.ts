/* ============================ TYTU≈ÅY ============================ */
export async function proposeConceptTitles(args: {
  subjectName: string;
  sectionTitle: string;
  topicTitle: string;
  topicDescription: string;
  count: number;
}) {
  const n = Math.min(Math.max(args.count ?? 5, 4), 6);
  const prompt =
    `Zwr√≥ƒá WY≈ÅƒÑCZNIE czysty tekst bez code fence‚Äô√≥w: ${n} tytu≈Ç√≥w, ` +
    `po JEDNYM tytule w KA≈ªDEJ osobnej linii, bez numer√≥w i punktor√≥w. ` +
    `Ka≈ºdy tytu≈Ç 4‚Äì7 s≈Ç√≥w, to kƒÖt interpretacyjny (nie kategoria). ` +
    `Unikaj s≈Ç√≥w-parasoli typu ‚ÄûMotyw‚Äù, ‚ÄûKontekst‚Äù, ‚Äû≈örodki stylistyczne‚Äù. ` +
    `Kontekst: Przedmiot=${args.subjectName}; Sekcja=${args.sectionTitle}; ` +
    `Temat=${args.topicTitle}; OpisTematu=${args.topicDescription}.`;

  const out = await generateMarkdown(prompt);
  const titles = out
    .split(/\r?\n/)
    .map(s => s.replace(/^[-*\d\.\)\s]+/, '').trim())
    .filter(Boolean)
    .slice(0, n);

  if (!titles.length) throw new Error('Brak tytu≈Ç√≥w z modelu');
  return titles;
}

